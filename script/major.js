window.addEventListener('load', function () {
	var graph = document.getElementById('point_graph');
	var buttons = graph.getElementsByClassName('point');

	var descDiv = document.getElementById('point_desc');
	var impressiveDiv = document.getElementById('point_impressive');
	var depressiveDiv = document.getElementById('point_depressive');
	const tableBody = document
		.getElementById('point_table')
		.getElementsByTagName('tbody')[0];

	var currentDesc = pointGraph[1];

	function setSemesterDesc(e) {
		const index = e.target.getAttribute('value');

		currentDesc = pointGraph[index];

		console.log(currentDesc);
		// 학기
		descDiv.getElementsByTagName('h3')[0].innerText = currentDesc.semester;
		// 점수
		descDiv.getElementsByClassName(
			'semester_point'
		)[0].innerText = `${currentDesc.point}/4.5`;

		// 점수 테이블
		tableBody.innerHTML = '';
		for (var i = 0; i < currentDesc.subject.length; i++) {
			var newTr = document.createElement('tr');
			var subName = document.createElement('td');
			var subPoint = document.createElement('td');

			subName.innerText = currentDesc.subject[i];
			subPoint.innerText = currentDesc.subPoint[i];

			newTr.appendChild(subName);
			newTr.appendChild(subPoint);

			tableBody.appendChild(newTr);
		}

		// 학기 소감

		// 감명수업

		// 아쉬운수업
	}

	for (var i = 0; i < buttons.length; i++) {
		buttons[i].addEventListener('click', setSemesterDesc);
	}
});
